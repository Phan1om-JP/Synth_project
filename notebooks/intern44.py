import matplotlib.pyplot as plt

def visualise_predictions(model, dataset, device, n=3):
    model.eval()
    indices = np.random.choice(len(dataset), n, replace=False)

    fig, axes = plt.subplots(n, 4, figsize=(16, 4 * n))
    if n == 1:
        axes = axes[np.newaxis, :]

    with torch.no_grad():
        for row, idx in enumerate(indices):
            mr, ct, mask = dataset[idx]
            pred = model(mr.unsqueeze(0).to(device)).cpu()

            mr_np   = mr.squeeze().numpy()
            ct_np   = postprocess_ct(ct.squeeze().numpy())
            pred_np = postprocess_ct(pred.squeeze().numpy())
            err_np  = np.abs(pred_np - ct_np) * mask.squeeze().numpy()

            axes[row, 0].imshow(mr_np,   cmap='gray');  axes[row, 0].set_title('MR input')
            axes[row, 1].imshow(ct_np,   cmap='gray');  axes[row, 1].set_title('CT real')
            axes[row, 2].imshow(pred_np, cmap='gray');  axes[row, 2].set_title('CT predicted')
            im = axes[row, 3].imshow(err_np, cmap='hot'); axes[row, 3].set_title('Abs error')
            plt.colorbar(im, ax=axes[row, 3])

            for ax in axes[row]:
                ax.axis('off')

    plt.tight_layout()
    plt.show()

visualise_predictions(generator, tr_dataset, CFG['device'], n=3)