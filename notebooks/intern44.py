TARGET_SIZE = 320

def pad_to_target(arr, target_h, target_w):
    h, w = arr.shape
    pad_top    = (target_h - h) // 2
    pad_bottom = target_h - h - pad_top
    pad_left   = (target_w - w) // 2
    pad_right  = target_w - w - pad_left
    return np.pad(arr, ((pad_top, pad_bottom), (pad_left, pad_right)), mode='constant', constant_values=0)

class SliceDataset2D(Dataset):
    def __init__(self, cache_dir, patient_list, target_size=TARGET_SIZE):
        self.slices      = []
        self.target_size = target_size
        for p in patient_list:
            pid   = p['patient_id']
            p_dir = os.path.join(cache_dir, pid)
            if not os.path.isdir(p_dir):
                continue
            for fname in sorted(os.listdir(p_dir)):
                if fname.endswith('.npy'):
                    self.slices.append(os.path.join(p_dir, fname))
        print(f'Dataset: {len(self.slices)} slices from {len(patient_list)} patients.')

    def __len__(self):
        return len(self.slices)

    def __getitem__(self, idx):
        packed = np.load(self.slices[idx])           # (3, H, W)
        mr     = pad_to_target(packed[0], self.target_size, self.target_size)
        ct     = pad_to_target(packed[1], self.target_size, self.target_size)
        mask   = pad_to_target(packed[2], self.target_size, self.target_size)
        mr   = torch.from_numpy(mr).unsqueeze(0)     # (1, 320, 320)
        ct   = torch.from_numpy(ct).unsqueeze(0)
        mask = torch.from_numpy(mask).unsqueeze(0)
        return mr, ct, mask

