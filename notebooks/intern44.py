def run_validation(model, loader, device):
    model.eval()
    mae_list, ssim_list, psnr_list = [], [], []

    with torch.no_grad():
        for mr, ct, mask in tqdm(loader, desc='Validation', leave=False):
            mr, ct, mask = mr.to(device), ct.to(device), mask.to(device)
            pred = model(mr)

            pred_np = postprocess_ct(pred.cpu().numpy()[:, 0])   # (B, H, W) in HU
            ct_np   = postprocess_ct(ct.cpu().numpy()[:, 0])
            mask_np = mask.cpu().numpy()[:, 0]

            for b in range(pred_np.shape[0]):
                m = mask_np[b] > 0
                if m.sum() == 0:
                    continue
                mae  = np.abs(pred_np[b][m] - ct_np[b][m]).mean()
                data_range = ct_np[b][m].max() - ct_np[b][m].min()

                p_sl = pred_np[b].copy()
                c_sl = ct_np[b].copy()
                p_sl[~m] = 0
                c_sl[~m] = 0
                ssim = ssim_fn(c_sl, p_sl, data_range=float(data_range))
                psnr = psnr_fn(c_sl, p_sl, data_range=float(data_range))

                mae_list.append(mae)
                ssim_list.append(ssim)
                psnr_list.append(psnr)

    return {
        'mae':  float(np.mean(mae_list)),
        'ssim': float(np.mean(ssim_list)),
        'psnr': float(np.mean(psnr_list)),
    }

